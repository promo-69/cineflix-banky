<script lang="ts">
	import type { Snippet } from 'svelte';
	import type { HTMLSelectAttributes } from 'svelte/elements';

	interface Props extends HTMLSelectAttributes {
		value?: any;
		children?: Snippet;
	}

	let {
		value = $bindable(),
		class: className = '',
		children,
		...rest
	}: Props = $props();
</script>

<select
	{...rest}
	class="input {className}"
	bind:value
>
	{#if children}
		{@render children()}
	{/if}
</select>
